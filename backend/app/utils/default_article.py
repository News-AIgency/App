"Štatistický úrad SR informuje o priemerných cenách 9 druhov palív pre motorové vozidlá, ku ktorým patria aj alternatívne druhy pohonných látok. Počas 41. týždňa bola cena plynu bioLNG na úrovni 2,394 eura za kilogram, medzitýždňovo zdražel o 8,3 centa. Vodík sa predával za 21,6 eura za kilogram a elektrická energia, slúžiaca na nabíjanie elektromobilov, bola na úrovni 0,41 eura za 1 kWh (rýchlosť nabíjania AC)."

"Priemerná cena motorovej nafty bola v 41. týždni medziročne2) nižšia o 14,5 %, 98 oktánový benzín bol lacnejší o 7,9 % a cena 95 oktánového benzínu bola oproti rovnakému obdobiu minulého roka nižšia o 7,1 %. Cena plynu LNG bola vyššia o 9,1 %, LPG bol drahší o 5,6 % a plyn CNG bol v porovnaní s vlaňajškom lacnejší o 8,6 %."


