"Štatistický úrad SR zverejňuje priemerné ceny celkovo 9 pohonných látok v SR v týždennej a mesačnej periodicite. Od januára 2024 bol zoznam sledovaných pohonných hmôt rozšírený o tri nové palivá - bioLNG (skvapalnený biometán), vodík (alternatíva za benzínové alebo naftové palivo) a elektrickú energiu slúžiacu na dobíjanie elektromobilov."

"Cieľom rozšírenia zverejňovaných pohonných látok je umožniť napríklad dopravcom a ďalším podnikateľským subjektom získať oficiálne údaje o priemerných cenách novo využívaných palív pre účtovné účely."

